package com.guide.pojo;

import java.io.Serializable;

public class SubmitHospital implements Serializable{
	/**
	 * 
	 */
	 private static final long serialVersionUID = -4142799840496173409L;
	private String City;
	private String HName;
	private String Hlevel;
	private String JudgeMan;
	private String JMTel;
	private String HUnitID;
	
	public String getCity() {
		return City;
	}
	public void setCity(String city) {
		City = city;
	}
	public String getHName() {
		return HName;
	}
	public void setHName(String hName) {
		HName = hName;
	}
	public String getHlevel() {
		return Hlevel;
	}
	public void setHlevel(String hlevel) {
		Hlevel = hlevel;
	}
	public String getJudgeMan() {
		return JudgeMan;
	}
	public void setJudgeMan(String linkMan) {
		JudgeMan = linkMan;
	}
	public String getJMTel() {
		return JMTel;
	}
	public void setJMTel(String jMTel) {
		JMTel = jMTel;
	}
	public String getHUnitID() {
		return HUnitID;
	}
	public void setHUnitID(String hUnitID) {
		HUnitID = hUnitID;
	}
	@Override
	public String toString() {
		return "SubmitHospital [City=" + City + ", HName=" + HName + ", Hlevel=" + Hlevel + ", JudgeMan=" + JudgeMan
				+ ", JMTel=" + JMTel + ", HUnitID=" + HUnitID + "]";
	}
	
}
